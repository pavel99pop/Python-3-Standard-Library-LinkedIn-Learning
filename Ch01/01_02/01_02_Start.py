# Python Comparison Operators

# TIPS:
# == --> is equal to
# <= --> is less than or equal to
# >= --> is greater than or equal to
# < --> is less than
# > --> is greater than
print(6 < 9)
print(9 < 6)

if 6 < 9:
    print('6 & 9 ;)')

# < --> is less than
kitten = 10
tiger = 50

if kitten < tiger:
    print('The kitten is smaller than the tiger')

# == --> is equal to
mouse = 1

if mouse < kitten and mouse < tiger:
    print('The mouse needs to eat more and gain some weight!')

# < --> is less than

#False --> 0
#True --> 1
# > --> is greater than
print(False > True)

# Looking for first mismatched letter
